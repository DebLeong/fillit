#include "fillit.h"

char	*ft_strnew(size_t size)
{
	int		i;
	char	*s;

	i = 0;
	if ((s = ft_memalloc(size + 1)))
	{
		while (s[i] != '\0')
		{
			s[i] = '.';
			i++;
		}
		return (s);
	}
	return (NULL);
}
